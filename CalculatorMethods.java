

public class CalculatorMethods {

    public int sum(int num1, int num2) { 
        int som = num1+num2;
        return som;
    }

    public int subt(int num1, int num2) { 
        int sub = num1-num2;
        return sub;
    }

    public double mult(int num1, int num2) { 
        int mul = num1*num2;
        return mul;
    }

    public double div(int num1, int num2) { 
        int div = num1/num2;
        return div;
    }

}